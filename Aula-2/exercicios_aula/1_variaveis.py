# int - número inteiro
numero_inteiro = 10
print(numero_inteiro)

# float - número decimal
numero_decimal = 10.5
print(numero_decimal)

# string - texto
texto = "Hello, World!"
print(texto)

# bool - booleano
booleano = True
print(booleano)

# list - lista
lista = [1, 2, 3, 4, 5]
print(lista)

# dict - dicionário
dicionario = {"nome": "João", "idade": 20}
print(dicionario)
