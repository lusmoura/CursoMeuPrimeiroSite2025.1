def soma(a, b):
    resultado = a + b
    print(f"A soma de {a} e {b} é: {resultado}")
    
soma(2, 3)
soma(10, 20)
soma("a", "b")
soma(1.5, 2.5)

def multiplica(a, b):
    resultado = a * b
    print(f"A multiplicação de {a} e {b} é: {resultado}")
    
multiplica(2, 3)
multiplica(10, 20)
multiplica("a", "b")
multiplica(1.5, 2.5)

def divide(a, b):
    resultado = a / b
    print(f"A divisão de {a} e {b} é: {resultado}")

divide(2, 3)
divide(10, 20)
divide("a", "b")
divide(1.5, 2.5)
divide(10, 0)  # Divisão por zero